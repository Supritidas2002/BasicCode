public class EvenNumbers {
    public static void main(String[] args) {
        int n = 2; // Initialize n to the first even number, 2
        
        // Use a while loop to print even numbers from 2 to 20
        while (n <= 20) {
            System.out.println(n); // Print the current even number
            n += 2; // Increment n by 2 to get the next even number
        }
    }
}
